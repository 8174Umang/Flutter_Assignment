void main() {
  // Declare two numbers
  double number1 = 10;
  double number2 = 5;

  double sum = number1 + number2;
  double difference = number1 - number2;
  double product = number1 * number2;
  double quotient = number1 / number2;

  // Display the results using print
  print("Addition: $number1 + $number2 = $sum");
  print("Subtraction: $number1 - $number2 = $difference");
  print("Multiplication: $number1 * $number2 = $product");
  print("Division: $number1 / $number2 = $quotient");
}
